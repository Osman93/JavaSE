package JavaDerslerim;
public class MathSınıfı {
    public static void main(String[] args){
    System.out.println("\tGerekli olabilecek bazı Math Sınıfı Metotları ");
    System.out.println(Math.PI);//Pi sayısının değerini bize verir..
    System.out.println(Math.E);//Exponential sayısını değerini bize verir.
    System.out.println(Math.IEEEremainder(5, 10));//Kalan sayıyı bize verir..
    System.out.println(Math.abs(-58));//Mutlak değerini bize verir..
    System.out.println(Math.ceil(2.2));//Ondalık sayıların tamsayı değerini 1 arttırır ondalık kısım kalmaz..
    System.out.println(Math.floor(7.9));//Ondalık sayıların tamsayı değerini 1 azaltır ondalık kısım kalmaz..
    System.out.println(Math.random());//0 ile 1 arasında rastgele sayı üretir..
    System.out.println(Math.log10(100));//10 tabanındaki sayının logaritmasını alır..
    System.out.println(Math.log(2));//e tabanındaki sayının logaritmasını alır..
    System.out.println(Math.max(8.9, 5.2));//Girilen iki sayıdan büyüğünü döndürür..
    System.out.println(Math.min(13.7,12.9));//Girilen iki sayıdan küçüğünü döndürür.
    System.out.println(Math.pow(2, 5));//ilk girilen taban diğeri üstür ve üs alma işlemini gerçekleştirir..
    System.out.println(Math.sqrt(9));//Girilen sayının karakökünü alır..
    
    }
}
