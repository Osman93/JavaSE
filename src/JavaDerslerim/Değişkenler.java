package JavaDerslerim;
public class Değişkenler {
    public static void main(String[] arg){
    //Değişken veri tiplerine bakalım...
        
        String metin="JavaSE";// String tipi kelime girmemize yarar..
        System.out.println("String değişkenimiz metin="+metin);
        //burada metin değişkenine JavaSE kelimesini ekledik ve yazdırdık..
    
        int tamsayi;
        tamsayi=10;//İnteger(tamsayi) veri tipi tamsayi değerlerini alabilir..
        System.out.println("İnt değişkenimiz tamsayi="+tamsayi);
        
        double kesirliSayi=5.5;//double veri tipi kesirli sayi değerlerini alcağımız zaman kullanmalıyız
        System.out.println("Double değişkenimiz kesirliSayi="+kesirliSayi);
        
        char karakter='a';// char(character) veri tipi karakter değeri almamızı sağlar.
        System.out.println("char değişkenimiz karakter="+karakter);
        
        
    }
}
